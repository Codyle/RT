#include "rt.h"

void	free_obj_vert(void **v, size_t num_v)
{
	if(v)
	{
		while (num_v--)
		{
			if (v[num_v])
				free(v[num_v]);
			v[num_v] = NULL;
		}
		free(v);
		v = NULL;
	}
}
