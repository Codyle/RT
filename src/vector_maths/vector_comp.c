#include "rt.h"

int	vcomp(t_vector v1, t_vector v2)
{
	if (v1.x == v2.x && v1.y == v2.y && v1.z == v2.z)
		return (0);
	else if (v1.x >= v2.x && v1.y >= v2.y && v1.z >= v2.z)
		return (1);
	else if (v1.x <= v2.x && v1.y <= v2.y && v1.z <= v2.z)
		return (-1);
	return (-2);
}
