#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*str;

	i = 0;
	str = ft_strnew(ft_strlen(s) + 1);
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	return (str);
}
